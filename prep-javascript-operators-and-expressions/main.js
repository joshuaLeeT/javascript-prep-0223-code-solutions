/*       multiply                          */

var number1 = 5;
var number2 = 10;

var product = number1 * number2;

console.log(product);
console.log(typeof product);

/*           subtraction                         */

var charge = 6;
var payment = 3;

var amountRemaining = charge - payment;
console.log(amountRemaining);
console.log(typeof amountRemaining);

/*        calculate average                           */

var test = 100;
var assignments = 90;
var final = 84;

var grade = test + assignments + final;
console.log(grade / 3);
console.log(typeof grade);

/*          string concatenation                         */

var firstName = 'Joshua';
var lastName = 'Tenny';

var fullName = firstName + lastName;
console.log(fullName);
console.log(typeof fullName);

/*               less than comparison                  */

var pH = 8;
var isAcidic = pH < 7;
console.log(isAcidic);
console.log(typeof isAcidic);

/*         equality comparison                          */

var numberOfSoldiers = 150;
var isSparta = numberOfSoldiers === 300;
console.log(isSparta);
console.log(typeof isSparta);

