/*       multiply                          */

const number1 = 5;
const number2 = 10;

const product = number1 * number2;

console.log(product);
console.log(typeof product);

/*           subtraction                         */

const charge = 6;
const payment = 3;

const amountRemaining = charge - payment;
console.log(amountRemaining);
console.log(typeof amountRemaining);

/*        calculate average                           */

const test = 100;
const assignments = 90;
const final = 84;

const grade = test + assignments + final;
console.log(grade / 3);
console.log(typeof grade);

/*          string concatenation                         */

const firstName = 'Joshua';
const lastName = 'Tenny';

const fullName = firstName + lastName;
console.log(fullName);
console.log(typeof fullName);

/*               less than comparison                  */

const pH = 8;
const isAcidic = pH < 7;
console.log(isAcidic);
console.log(typeof isAcidic);

/*         equality comparison                          */

const numberOfSoldiers = 150;
const isSparta = numberOfSoldiers === 300;
console.log(isSparta);
console.log(typeof isSparta);

